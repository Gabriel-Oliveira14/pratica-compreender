/* 
    var, let e const : são variáveis que tem como função armazenar dados, que pode ser utilizado durante o código, quando é selecionado o identicado da variável.

    var: é variável global que já não é mais recomendado na criação de seu código.
    
    let: é uma variável que só afeta o escopo do código em que está apresentado(com exeção depedendo da hirarquia de classe)..

    const: é bem diferente das outras variável, devido a ser uma variável que ao ter seu valor definido não pode ser mais alterada.Além disso, assim como o let so afeta seu escopo(com exeção depedendo da hirarquia de classe).
*/
var  num1 = 1;
let  num2 = 2;
const num3 = 3;

